INSERT INTO public.messages (chat_id, sender_id, text, created_at, read_at)
SELECT
    ch.id,
    sender.id,
    d.message_text,
    d.created_at::timestamp,
    NULLIF(d.read_at, '')::timestamp
FROM data d
JOIN public.companies c ON c.name = d.company_name
JOIN public.vacancies v ON v.company_id = c.id AND v.title = d.vacancy_title
JOIN public.users applicant_user ON applicant_user.email = d.applicant_email
JOIN public.applicants a ON a.id = applicant_user.applicant_id
JOIN public.resumes r ON r.applicant_id = a.id AND r.is_active = TRUE
JOIN public.applications app ON app.vacancy_id = v.id AND app.resume_id = r.id
JOIN public.chats ch ON ch.application_id = app.id
JOIN public.users sender ON sender.email = d.sender_email
WHERE NOT EXISTS (
    SELECT 1 FROM public.messages m
    WHERE m.chat_id = ch.id
      AND m.sender_id = sender.id
      AND COALESCE(m.text, '') = COALESCE(d.message_text, '')
      AND m.created_at = d.created_at::timestamp
);


