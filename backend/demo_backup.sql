INSERT INTO public.applications (
    vacancy_id, resume_id, status, cover_letter, created_at, updated_at
)
SELECT
    v.id,
    r.id,
    d.status_name,
    d.cover_letter,
    d.created_at::timestamp,
    d.updated_at::timestamp
FROM data d
JOIN public.companies c ON c.name = d.company_name
JOIN public.vacancies v ON v.company_id = c.id AND v.title = d.vacancy_title
JOIN public.users u ON u.email = d.applicant_email
JOIN public.applicants a ON a.id = u.applicant_id
JOIN public.resumes r ON r.applicant_id = a.id AND r.is_active = TRUE
